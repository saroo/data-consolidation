<h1>Welcome</h1>

<p>The Alumni Data Consolidation project is an initiative by the Director, IIT Roorkee along with Shantanu Agarwal (batch 2002, IIT Roorkee) and Information Management Group to create a single and master database of the Alumni of Indian Institute of Technology Roorkee (formerly University of Roorkee). The single database would serve to empower the Dean Alumni Affairs Office with a proper search facility and help him to cater to alumni requests in a much better way.
</p>
<p>
How this project would be useful for Alumni?

<ul>    
<li>The Email-id being created for the Data Consolidation project would later on serve as the sole credentials for the alumni to access all e-resoucres available like Sampark, ThinkTank, Alumni Mentorship and others.</li>
<li>The Database would allow for a flexible search feature which help you to re-connect and communicate with your friends and get in touch with them.</li>

</ul>
